package org.example.core;

import java.lang.reflect.Method;

/**
 * Интерфейс для предоставления аргументов методам тестов.
 * <p>
 * Реализуется аннотационными обработчиками (например, {@code CsvSourceHandler}),
 * которые способны анализировать параметры метода и генерировать аргументы
 * для вызова (например, из CSV-строки или других источников).
 * <p>
 * Используется {@link org.example.handlers.TestHandler} для параметризации вызовов
 * без жёсткой привязки к конкретному источнику аргументов.
 */
public interface ArgumentProvider {

    /**
     * Возвращает массив аргументов для вызова указанного метода.
     *
     * @param method  метод, для которого нужно предоставить аргументы
     * @param context текущий контекст выполнения тестов
     * @return массив аргументов для передачи в метод; если нет аргументов — пустой массив
     */
    Object[] getArgumentsFor(Method method, TestContext context);
}
